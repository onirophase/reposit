package composants;

public class Bidule {
	String truc;
	String machin;
	String chose;
	public String getTruc() {
		return truc;
	}
	public void setTruc(String truc) {
		this.truc = truc;
	}
	public String getMachin() {
		return machin;
	}
	public void setMachin(String machin) {
		this.machin = machin;
	}
	public String getChose() {
		return chose;
	}
	public void setChose(String chose) {
		this.chose = chose;
	}
}
